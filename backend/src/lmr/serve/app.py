def create_app(config_path: str = "/app/config/datasets.yaml") -> FastAPI:
    config = load_config(config_path)
    app = FastAPI(title="LMR Data Platform", version="0.1.0")
    app.state.config = config

    # CORS — required for Prism cross-origin requests
    app.add_middleware(
        CORSMiddleware,
        allow_origins=config.serve.cors_origins,
        allow_methods=["GET"],
        allow_headers=["*"],
    )

    # Return empty transparent tile for out-of-bounds tile requests
    @app.exception_handler(TileOutsideBounds)
    async def _tile_outside_bounds(request: Request, exc: TileOutsideBounds):
        return Response(content=_EMPTY_PNG, media_type="image/png")

    @app.exception_handler(Exception)
    async def _general_error_handler(request: Request, exc: Exception):
        if "/tiles/" in request.url.path:
            return Response(content=_EMPTY_PNG, media_type="image/png")
        logger.exception("Unhandled error on %s", request.url.path)
        return Response(content="Internal server error", status_code=500)

    # Custom API routes
    from lmr.serve.routes import router

    app.include_router(router)

    # TiTiler COG tile server
    from lmr.serve.titiler_setup import create_cog_router

    app.include_router(create_cog_router(), prefix="/cog", tags=["COG Tiles"])

    return app
